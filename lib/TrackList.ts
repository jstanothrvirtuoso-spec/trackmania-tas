export function getGameSetOptions(game: string): GameSet[] {
  return [...new Set(
    Object.values(TRACKS)
      .filter(track => track.game === game)
      .map(track => track.gameSet)
  )];
};

export function getEnvironmentOptions(game: string): typeof ENVIRONMENT[number][] {

  const order = new Map(ENVIRONMENT.map((env, index) => [env, index]));

  return [...new Set(
    Object.values(TRACKS)
      .filter(track => track.game === game)
      .map(track => track.environment)
  )].sort((a, b) => (order.get(a) ?? Infinity) - (order.get(b) ?? Infinity));
};
